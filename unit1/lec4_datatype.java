public class lec4_datatype {
    public static void main(String[] args) {
        byte a=-98;
        System.out.println(a);
        char b='k';    //can only hold a character not a string
        System.out.println(b);
        short c=5698;
        System.out.println(c);
        boolean d=true;
        System.out.println(d);
        float e=9.5f;  //float e=9.5 is double data type not float
        System.out.println(e);
        double f=98.6;
        System.out.println(f);
        //always use L when to declare long value
        // always use d when to declare double value anf f for float value
        // int g=98.6;   error
        // System.out.println(g);

        // it shows error if you declare any variable and do not use it in code so use every variable if you declare it

    }
    
}
